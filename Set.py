#A set in Python is a collection that stores unique (no duplicates) and unordered elements.

# Empty set
s = set()
print(s)

# Set with values
fruits = {"apple", "banana", "mango", "5"}
print(fruits)
print(type(fruits))
print(len(fruits))


#duplicate value
numbers = {1, 2, 3, 2, 1, 4}
print(numbers)
print(len(numbers))


